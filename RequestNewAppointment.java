    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int SelectIndex = jTable1.getSelectedRow();
        String docname= (String) d1.getValueAt(SelectIndex,0),doc_id="";
        try 
        {
            pst= con.prepareStatement("SELECT * FROM doctor WHERE DocName='"+docname+"'");
            rs = pst.executeQuery();
            rs.next();
            doc_id = rs.getString("DocID");
        }
        catch (SQLException ex) 
        {
            Logger.getLogger(RequestNewAppointment.class.getName()).log(Level.SEVERE, null, ex);
        }
        try 
        {
            String date=jTextField1.getText();
            pst= con.prepareStatement("INSERT INTO `appointmentreq`(`PatID`, `DocID`, `Date`, `Approved`) VALUES ('"+pid+"','"+doc_id+"','"+date+"','NO')");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Requested!!");
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(RequestNewAppointment.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

