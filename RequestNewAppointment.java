      public void display() throws SQLException
      {
          pst= con.prepareStatement("SELECT * FROM doctor");
          rs=pst.executeQuery();
          DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()!=false)
            {
                Vector v=new Vector();
                v.add(rs.getString("DocName"));
                v.add(rs.getString("Department"));
                df.addRow(v);
                
            }
      }

